const raioCirculo = 5
const pi = 3.14
const comprimentoCirculo = (2 * pi * raioCirculo).toFixed(1)
const areaCirculo = pi * Math.pow(raioCirculo, 2)

console.log(`Tendo um círculo de raio ${raioCirculo}, obtêm-se um comprimento de aproximadamente ${comprimentoCirculo} e uma área de ${areaCirculo}.`)